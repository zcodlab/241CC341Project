package uni.isw.designpatterns.dto.dto;

public class CarDto {
    public String make;
    public int seatCount;
    public String type;

    public CarDto() {
    }

    public String getMake() {
        return make;
    }

    public int getSeatCount() {
        return seatCount;
    }

    public String getType() {
        return type;
    }
    
    
}
